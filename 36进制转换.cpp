#include <bits/stdc++.h> 
using namespace std;

const int N = 20;

int main() {
    string s = "2021ABCD";

// 定义一个整型数组 a，用来存储字符串中每个字符对应的数值
// 例如，'A' 会被存为 10
//因为在十六进制中,A是 9 之后的下一个数10
    int a[N];
// 第一个循环：遍历字符串，将每个字符转换为对应的数值并存入数组 a
// 注意：这里从 a[1] 开始存储，这是一种常见的编程技巧，方便后续计算
    for (int i = 0; i < s.length(); ++i) {
        // 使用 toupper 函数，确保无论输入是大写还是小写字母都能处理
        char c = toupper(s[i]);//它会将传入的字符转换成大写 

        if ('0' <= c && c <= '9') {
            // 如果是数字字符，减去 '0' 的ASCII码得到其数值
            // 例如 '2' - '0' = 2
            a[i + 1] = c - '0';
        } else {
            // 如果是字母字符，减去 'A' 再加 10 得到其数值
            // 例如 'A' - 'A' + 10 = 10, 'B' - 'A' + 10 = 11
            a[i + 1] = c - 'A' + 10;
        }
    }

    // 定义一个长整型变量 x 来存储最终的十进制结果
    // 使用 long long 是为了防止大数溢出
    long long x = 0;

// 第二个循环：使用“按权展开相加”法计算十进制值
// 遍历数组 a，将十六进制转换为十进制
    for (int i = 1; i <= s.length(); ++i) {
        // 每次循环，将当前结果乘以16（进位），然后加上下一位的值
        x = x * 16 + a[i];
    }
//x 中存储的就是整个十六进制字符串所对应的完整十进制数值 

    cout << x << '\n';

    return 0;
}

